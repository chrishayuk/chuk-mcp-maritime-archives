def build_cargo() -> list[dict]:
    """Return ~200 curated VOC cargo manifest records."""
    records = []
    for row in CARGO_RAW:
        (
            num,
            voyage_id,
            commodity,
            commodity_dutch,
            quantity,
            quantity_kg,
            origin,
            destination,
            date,
            value_guilders,
            notes,
        ) = row
        records.append(
            {
                "cargo_id": f"voc_cargo:{num:06d}",
                "voyage_id": voyage_id,
                "commodity": commodity,
                "commodity_dutch": commodity_dutch,
                "quantity": quantity,
                "quantity_kg": quantity_kg,
                "origin": origin,
                "destination": destination,
                "date": date,
                "value_guilders": value_guilders,
                "notes": notes,
            }
        )
    return records


