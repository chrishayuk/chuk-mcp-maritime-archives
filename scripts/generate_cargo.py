def main() -> None:
    args = parse_args("Generate VOC cargo manifest data")

    print("=" * 60)
    print("VOC Cargo Data Generation -- chuk-mcp-maritime-archives")
    print("=" * 60)
    print(f"\nData directory: {DATA_DIR}\n")

    if not args.force and is_cached(OUTPUT, args.cache_max_age):
        print(f"Using cached {OUTPUT.name} (use --force to regenerate)")
        return

    DATA_DIR.mkdir(parents=True, exist_ok=True)

    # -- Generate cargo records --
    print("Generating VOC cargo manifest records ...")
    cargo = build_cargo()
    with open(OUTPUT, "w") as f:
        json.dump(cargo, f, indent=2, ensure_ascii=False)
    print(f"  {OUTPUT}")
    print(f"  {len(cargo)} records written ({OUTPUT.stat().st_size:,} bytes)")

    # -- Validate cargo_id uniqueness and sequential numbering --
    print("\nValidation:")
    expected_ids = {f"voc_cargo:{i:06d}" for i in range(1, len(cargo) + 1)}
    actual_ids = {c["cargo_id"] for c in cargo}
    assert expected_ids == actual_ids, (
        f"cargo_id mismatch.\n"
        f"  Missing: {expected_ids - actual_ids}\n"
        f"  Extra:   {actual_ids - expected_ids}"
    )
    print(f"  cargo_id uniqueness: OK ({len(actual_ids)} unique IDs, sequential 1-{len(cargo)})")

    # -- Validate all required fields present --
    required_fields = [
        "cargo_id",
        "voyage_id",
        "commodity",
        "commodity_dutch",
        "quantity",
        "quantity_kg",
        "origin",
        "destination",
        "date",
        "value_guilders",
        "notes",
    ]
    for i, rec in enumerate(cargo):
        for field in required_fields:
            assert field in rec, f"Record {i + 1} missing field: {field}"
    print(f"  Required fields: OK (all {len(required_fields)} fields present in every record)")

    # -- Print summary: commodity distribution --
    commodity_counts: dict[str, int] = {}
    commodity_values: dict[str, int] = {}
    for rec in cargo:
        c = rec["commodity"]
        commodity_counts[c] = commodity_counts.get(c, 0) + 1
        commodity_values[c] = commodity_values.get(c, 0) + rec["value_guilders"]

    print(f"\n  Commodity distribution ({len(commodity_counts)} categories):")
    for c in sorted(commodity_counts, key=lambda x: commodity_counts[x], reverse=True):
        print(f"    {c:20s}  {commodity_counts[c]:3d} records  {commodity_values[c]:>12,} guilders")

    # -- Total value --
    total_value = sum(rec["value_guilders"] for rec in cargo)
    print(f"\n  Total cargo value: {total_value:,} guilders")

    # -- Date range --
    dates = sorted(rec["date"] for rec in cargo)
    print(f"  Date range: {dates[0]} to {dates[-1]}")

    # -- Decade distribution --
    decade_counts: dict[str, int] = {}
    for rec in cargo:
        decade = rec["date"][:3] + "0s"
        decade_counts[decade] = decade_counts.get(decade, 0) + 1
    print("\n  Decade distribution:")
    for decade in sorted(decade_counts):
        print(f"    {decade}: {decade_counts[decade]:3d} records")

    print(f"\n{'=' * 60}")
    print("VOC cargo data generation complete!")
    print(f"  Records: {len(cargo)}")
    print(f"  Output:  {OUTPUT}")
    print(f"{'=' * 60}")


if __name__ == "__main__":
    main()
