    voyages = []
    for row in VOYAGES_RAW:
        (num, ship, capt, tons, dep, dep_port, arr, dest, division, cargo, fate, particulars) = row
        voyages.append(
            {
                "voyage_id": f"eic:{num:04d}",
                "ship_name": ship,
                "captain": capt,
                "tonnage": tons,
                "departure_date": dep,
                "departure_port": dep_port,
                "arrival_date": arr,
                "destination_port": dest,
                "company_division": division,
                "cargo_description": cargo,
                "fate": fate,
                "particulars": particulars,
                "archive": ARCHIVE,
                "is_curated": True,
            }
        )
    return voyages


