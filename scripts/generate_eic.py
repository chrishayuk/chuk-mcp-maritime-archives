def build_wrecks() -> list[dict]:
    """Return ~120 curated EIC wreck records."""
    wrecks = []
    for row in WRECKS_RAW:
        (
            num,
            vid,
            ship,
            loss_date,
            cause,
            location,
            region,
            status,
            lat,
            lon,
            unc,
            depth,
            tons,
            particulars,
        ) = row
        wrecks.append(
            {
                "wreck_id": f"eic_wreck:{num:04d}",
                "voyage_id": vid,
                "ship_name": ship,
                "loss_date": loss_date,
                "loss_cause": cause,
                "loss_location": location,
                "region": region,
                "status": status,
                "position": {"lat": lat, "lon": lon, "uncertainty_km": unc},
                "depth_estimate_m": depth,
                "tonnage": tons,
                "archive": ARCHIVE,
                "particulars": particulars,
                "is_curated": True,
            }
        )
    return wrecks


